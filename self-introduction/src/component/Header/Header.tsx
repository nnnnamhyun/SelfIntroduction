interface HedaerProps  {
    brand:string;
    rightLinks: ReactFragment;
    changeColorOnScroll : any;
    color : string;
    fixed : boolean;
    absolute : boolean;
}



export default function Header(props:HedaerProps) {
  const classes = useStyles();
  const [mobileOpen, setMobileOpen] = React.useState(false);


  const { color, rightLinks, brand, fixed, absolute } = props;

  const appBarClasses = classNames({
    [classes.appBar]: true,
    [classes.transparent] : true,
    [classes.white]: false,
    [classes.absolute]: absolute,
    [classes.fixed]: fixed
  });

  React.useEffect(() => {
    if (props.changeColorOnScroll) {
      window.addEventListener("scroll", headerColorChange);
    }
    return function cleanup() {
      if (props.changeColorOnScroll) {
        window.removeEventListener("scroll", headerColorChange);
      }
    };
  });
  const headerColorChange = () => {
    const { color, changeColorOnScroll } = props;
    const windowsScrollTop = window.pageYOffset;
    if (windowsScrollTop > changeColorOnScroll.height) {
      document.body
        .getElementsByTagName("header")[0]
        .classList.add(classes.white);
    } else {
      document.body
        .getElementsByTagName("header")[0]
        .classList.remove(classes.white);
    }
  };
  const handleDrawerToggle = () => {
    setMobileOpen(!mobileOpen);
  };

  const brandComponent = <Button className={classes.brandTitle}>{props.brand}</Button>;
  return (
    <React.Fragment>
      <AppBar className={appBarClasses}>
      <Toolbar className={classes.toolbarContainer}>
        <div className={classes.flex}>
            {brandComponent}
        </div>
        <Hidden smDown implementation="css">
          <HeaderLinks/>
        </Hidden>
        <Hidden mdUp>
          <IconButton
            color="inherit"
            aria-label="open drawer"
            onClick={handleDrawerToggle}
          >
            <Menu />
          </IconButton>
        </Hidden>
      </Toolbar>
      <Hidden mdUp implementation="js">
        <Drawer
          variant="temporary"
          anchor={"right"}
          open={mobileOpen}
          classes={{
            paper: classes.drawerPaper
          }}
          onClose={handleDrawerToggle}
        >
          <div className={classes.appResponsive}>
            <HeaderLinks/>
          </div>
        </Drawer>
      </Hidden>
    </AppBar>
    </React.Fragment>
  );
}
